<?php
// اتصال به پایگاه داده با استفاده از mysqli

// مشخصات اتصال به دیتابیس
$host = "localhost"; // آدرس سرور دیتابیس
$username = "root"; // نام کاربری دیتابیس
$password = ""; // رمز عبور دیتابیس (خالی برای لوکال)
$database = "game_net_db"; // نام دیتابیس ما

// ایجاد اتصال به دیتابیس
$conn = mysqli_connect($host, $username, $password, $database);

// بررسی موفقیت اتصال
if (!$conn) {
    // اگر اتصال موفق نبود، پیام خطا نشان داده می‌شود و برنامه متوقف می‌شود
    die("خطا در اتصال به پایگاه داده: " . mysqli_connect_error());
}

// اگر اتصال موفق بود، هیچ پیامی نشان داده نمی‌شود و اسکریپت ادامه پیدا می‌کند
